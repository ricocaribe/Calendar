package com.jmrp.calendar;

/**
 * String resources required for Calendar instance
 */
final public class CalendarStringResources {

    private final String mEmptyLabel;

    /**
     * Instantiates a new String resources.
     * @param emptyLabel the empty label
     */
    public CalendarStringResources(String emptyLabel) {
        this.mEmptyLabel = emptyLabel;
    }

    /**
     * Gets empty label.
     *
     * @return the empty label
     */
    public String getKeyame() {
        return mEmptyLabel;
    }
}
